  const handleFormSubmit = async (data: ContactFormData) => {
    setSubmitStatus('idle'); // Reset status on new submission attempt
    console.log('Form data submitted:', data);

    // Call API route to send email
    try {
      const response = await fetch('/api/contact', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify(data),
      });

      if (response.ok) {
        setSubmitStatus('success');
        // Optionally clear form data here if ContactForm component doesn't handle it
        // This might require passing a reset function to ContactForm
      } else {
        const errorData = await response.json();
        console.error('Submission failed:', errorData);
        setSubmitStatus('error');
      }
    } catch (error) {
      console.error('Submission error:', error);
      setSubmitStatus('error');
    }
  };

